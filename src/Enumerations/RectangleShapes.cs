namespace DXPlus;

/// <summary>
/// Rectangle shapes that can be added to the document
/// </summary>
public enum RectangleShapes
{
    /// <summary>
    /// Rectangle
    /// </summary>
    Rect,

    /// <summary>
    /// Rounded rectangle
    /// </summary>
    RoundRect,

    /// <summary>
    /// Snip rectangle
    /// </summary>
    Snip1Rect,

    /// <summary>
    /// Snip 2 sides same rectangle
    /// </summary>
    Snip2SameRect,

    /// <summary>
    /// Snip diagonal rectangle
    /// </summary>
    Snip2DiagRect,

    /// <summary>
    /// Snip rounded rectangle
    /// </summary>
    SnipRoundRect,

    /// <summary>
    /// Rounded 1 side rectangle
    /// </summary>
    Round1Rect,

    /// <summary>
    /// Rounded 2 side rectangle
    /// </summary>
    Round2SameRect,

    /// <summary>
    /// Rounded 2 side diagonal rectangle
    /// </summary>
    Round2DiagRect
};