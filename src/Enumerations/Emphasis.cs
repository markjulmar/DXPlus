namespace DXPlus;

/// <summary>
/// Specifies possible types of emphasis marks which may be displayed for each non-space character in a run.
/// This character is rendered above or below the character glyph
/// </summary>
public enum Emphasis
{
    /// <summary>
    /// None
    /// </summary>
    None,

    /// <summary>
    /// Dot
    /// </summary>
    Dot,

    /// <summary>
    /// Comma
    /// </summary>
    Comma,

    /// <summary>
    /// Circle
    /// </summary>
    Circle,

    /// <summary>
    /// Under-dot
    /// </summary>
    UnderDot
}