namespace DXPlus.Charts;

/// <summary>
/// Specifies the possible groupings for a bar chart.
/// </summary>
public enum BarGrouping
{
    /// <summary>
    /// Stacked by percentage
    /// </summary>
    PercentStacked,
    /// <summary>
    /// Clustered grouping
    /// </summary>
    Clustered,
    /// <summary>
    /// Standard grouping
    /// </summary>
    Standard,
    /// <summary>
    /// Stacked grouping
    /// </summary>
    Stacked
}