namespace DXPlus.Charts;

/// <summary>
/// Specifies the kind of grouping for a column, line, or area chart.
/// </summary>
public enum Grouping
{
    /// <summary>
    /// Grouped by stacked percentage
    /// </summary>
    PercentStacked,
    /// <summary>
    /// Stacked
    /// </summary>
    Stacked,
    /// <summary>
    /// Standard grouping
    /// </summary>
    Standard
}