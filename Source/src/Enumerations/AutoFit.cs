namespace DXPlus
{

    /// <summary>
    /// How a Table should auto resize.
    /// </summary>
    public enum AutoFit
    {
        /// <summary>
        /// Autofit to Table contents.
        /// </summary>
        Contents,

        /// <summary>
        /// Autofit to Window.
        /// </summary>
        Window,

        /// <summary>
        /// Autofit to Column width.
        /// </summary>
        ColumnWidth,

        /// <summary>
        ///  Autofit to Fixed column width
        /// </summary>
        Fixed
    };
}